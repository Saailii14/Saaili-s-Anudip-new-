public class Switchcase {

    public static void main(String[] args) {
        String name = "Saaili";

        switch (name) {
            case "Saaili":
                System.out.println("Hello, Saaili!");
                break;

            case "Harsh":
                System.out.println("Hi, Harsh!");
                break;

            case "Arjun":
                System.out.println("Hey, Arjun!");
                break;

            default:
                System.out.println("Name not found.");
        }
    }
}


